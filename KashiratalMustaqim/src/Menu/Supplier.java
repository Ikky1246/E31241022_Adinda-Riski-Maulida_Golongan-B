    private void tampilkan() {
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{
            "ID", "Kode Supplier", "Nama Perusahaan", "Barang Dijual", "No. Telp", "Alamat"
        });
        TB_Supplier.setModel(model);

        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/kashiratal_mustaqim", "root", "");
            String query = "SELECT id, kode_supplier, nama_perusahaan, barang_dijual, no_tlp, alamat FROM supplier";
            PreparedStatement stmt = conn.prepareStatement(query);
            ResultSet rs = stmt.executeQuery();

            while (rs.next()) {
                String id = rs.getString("id");
                String kode_supplier = rs.getString("kode_supplier");
                String nama_perusahaan = rs.getString("nama_perusahaan");
                String barang_dijual = rs.getString("barang_dijual");
                String no_tlp = rs.getString("no_tlp");
                String alamat = rs.getString("alamat");

                model.addRow(new Object[]{
                    id, kode_supplier, nama_perusahaan, barang_dijual, no_tlp, alamat
                });
            }

            rs.close();
            stmt.close();
            conn.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data supplier: " + e.getMessage());
        }
    }

