    private boolean validateInput() {
        if (kd_sup.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Kode Supplier tidak boleh kosong");
            return false;
        }
        if (nm_p.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama Perusahaan tidak boleh kosong");
            return false;
        }
        if (barjul.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Barang di Jual tidak boleh kosong");
            return false;
        }
        if (no_tlp.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No. Telp tidak boleh kosong");
            return false;
        }
        if (alamat.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Alamat tidak boleh kosong");
            return false;
        }
        return true;
    }

    private void saveData() {
        if (!validateInput()) {
            return; // Hentikan jika input tidak valid
        }

        String query = "INSERT INTO supplier (kode_supplier, nama_perusahaan, barang_dijual, no_tlp, alamat) VALUES (?, ?, ?, ?, ?)";

        try (
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/kashiratal_mustaqim", "root", ""); PreparedStatement ps = con.prepareStatement(query)) {
            ps.setString(1, kd_sup.getText().trim());
            ps.setString(2, nm_p.getText().trim());
            ps.setString(3, barjul.getText().trim());
            ps.setString(4, no_tlp.getText().trim());
            ps.setString(5, alamat.getText().trim());

            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data supplier berhasil disimpan!");

            // Kosongkan semua input field
            kd_sup.setText("");
            nm_p.setText("");
            barjul.setText("");
            no_tlp.setText("");
            alamat.setText("");

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage());
        }
    }
