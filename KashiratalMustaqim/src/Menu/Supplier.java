    private void jB_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_DeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = TB_Supplier.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris yang ingin dihapus!");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) TB_Supplier.getModel();
// Ambil ID dari kolom pertama (kolom ke-0)
        String idSupplier = model.getValueAt(selectedRow, 0).toString();

// Konfirmasi penghapusan
        int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                // Hapus data dari database berdasarkan ID
                Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/kashiratal_mustaqim", "root", "");
                String query = "DELETE FROM supplier WHERE id = ?";
                PreparedStatement ps = conn.prepareStatement(query);
                ps.setString(1, idSupplier);
                ps.executeUpdate();

                // Hapus baris dari tabel
                model.removeRow(selectedRow);

                JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Kesalahan saat menghapus data: " + e.getMessage());
            }
        }

    }//GEN-LAST:event_jB_DeleteActionPerformed

    private void jB_EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_EditActionPerformed
        // TODO add your handling code here: 
        String kode_supplier = kd_sup.getText().trim(); // read-only, tidak bisa diedit user
        String nama_perusahaan = nm_p.getText().trim();
        String barang_dijual = barjul.getText().trim();
        String notlp = no_tlp.getText().trim();
        String almt = alamat.getText().trim();

// Kode untuk koneksi ke database
        Connection conn = null;
        PreparedStatement pst = null;

        try {
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/kashiratal_mustaqim", "root", "");

            // SQL query tanpa update ID dan kode_supplier
            String sql = "UPDATE supplier SET nama_perusahaan = ?, barang_dijual = ?, no_tlp = ?, alamat = ? WHERE kode_supplier = ?";

            pst = conn.prepareStatement(sql);
            pst.setString(1, nama_perusahaan);
            pst.setString(2, barang_dijual);
            pst.setString(3, notlp);
            pst.setString(4, almt);
            pst.setString(5, kode_supplier); // dipakai sebagai WHERE

            int updatedRows = pst.executeUpdate();

            if (updatedRows > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Data tidak ditemukan untuk diupdate!", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            try {
                if (pst != null) {
                    pst.close();
                }
                if (conn != null) {
                    conn.close();
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }

        tampilkan(); // Refresh tabel

    }//GEN-LAST:event_jB_EditActionPerformed

